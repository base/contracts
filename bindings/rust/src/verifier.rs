        pub fn nullified(&self) -> alloy_contract::SolCallBuilder<&P, nullifiedCall, N> {
            self.call_builder(&nullifiedCall)
        }
        ///Creates a new call builder for the [`nullify`] function.
        pub fn nullify(&self) -> alloy_contract::SolCallBuilder<&P, nullifyCall, N> {
            self.call_builder(&nullifyCall)
        }
